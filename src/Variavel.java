
public class Variavel {

	public static void main(String[] args) {
		byte idade = 17;
		short duracaoFilme = 12345;
		int distancia = 4;
		long qtdPessoas = 20;
		
		double peso = 52.3;
		float altura = 1.70f;
		
		boolean contaPaga = false;
		
		char simbolo = '"';
		
		System.out.println("Idade: " + idade);
		System.out.println("Duração do filme: " + duracaoFilme + "minutos.");
		System.out.println("A distância entre as mesas é de " + distancia + " metros.");
		System.out.println("A quantidade de aluno é de " + qtdPessoas + " pessoas.");
		System.out.print("O peso dele é de " + peso);
		System.out.print("  e a altura é de " + altura);
		System.out.println("A conta foi paga? " + contaPaga);
		System.out.println("Símbolo de aspas: " + simbolo);
	}
}
